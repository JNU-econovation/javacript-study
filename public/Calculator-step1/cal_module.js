function plus(n1, n2) {
    n1=parseInt(n1);
    n2=parseInt(n2);
    return n1+n2;
}

function minus(n1, n2) {
    n1=parseInt(n1);
    n2=parseInt(n2);
    return n1-n2;
}

function multi(n1, n2) {
    n1=parseInt(n1);
    n2=parseInt(n2);
    return n1*n2;
}

function divide(n1, n2) {
    n1=parseInt(n1);
    n2=parseInt(n2);
    return parseFloat(n1/n2);
}

function mod(n1, n2) {
    n1=parseInt(n1);
    n2=parseInt(n2);
    return n1%n2;
}